static void tyan_s5549_bmc_i2c_init(AspeedMachineState *bmc)
{
    AspeedSoCState *soc = bmc->soc;

    I2CBus *i2c[12] = {};
    for (int i = 0; i < ARRAY_SIZE(i2c); i++) {
        i2c[i] = aspeed_i2c_get_bus(&soc->i2c, i);
    }

    /* FRU eeprom */
    at24c_eeprom_init_rom(i2c[6], 0x50, 128 * KiB,
                          fru_tyan_s5549_bin, fru_tyan_s5549_bin_len);

    /* 2 x PSU EEPROM */
    at24c_eeprom_init_rom(i2c[7], 0x38, 128 * KiB,
        fru_supermicro_pws_920p_0_bin, fru_supermicro_pws_920p_0_bin_len);

    at24c_eeprom_init_rom(i2c[7], 0x39, 128 * KiB,
        fru_supermicro_pws_920p_1_bin, fru_supermicro_pws_920p_1_bin_len);


    /* TMP75, TMP105 compatible */
    i2c_slave_create_simple(i2c[6], TYPE_TMP105, 0x49);
    i2c_slave_create_simple(i2c[6], TYPE_TMP105, 0x4a);

}

